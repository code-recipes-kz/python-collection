"""
Функция remove_repeating_numbers принимает на вход список чисел lst и максимальное количество повторений n. Она создает новый список result, в котором каждое число из lst встречается не более n раз, сохраняя при этом порядок чисел из исходного списка. Функция использует словарь occurrences, чтобы отслеживать количество повторений каждого числа. Если число уже встречалось n раз, оно не добавляется в результат. В конце функция возвращает список result без повторений более n раз.

Создание пустого списка result, который будет содержать результат.
Создание пустого словаря occurrences, который будет использоваться для отслеживания количества повторений каждого числа.
Итерация по каждому числу num из исходного списка lst с помощью цикла for.
Для каждого числа num проверка, сколько раз оно уже встречалось в result с использованием метода occurrences.get(num, 0). Если количество повторений числа num в result меньше n, то оно добавляется в result с помощью метода result.append(num), и количество повторений числа num в occurrences увеличивается на 1.
Повторение шагов 3-4 для всех чисел из исходного списка lst.
Возврат результата - списка result без повторений более n раз.
"""

# tags: Lists, Fundamentals
# kyu: 6

def delete_nth(lst, n):
    """
    Функция, удаляющая повторяющиеся числа из списка не более n раз.

    :param lst: список чисел
    :type lst: list
    :param n: максимальное количество повторений для каждого числа
    :type n: int
    :return: новый список без повторений более n раз
    :rtype: list
    """
    result = []
    occurrences = {}
    for num in lst:
        if occurrences.get(num, 0) < n:
            result.append(num)
            occurrences[num] = occurrences.get(num, 0) + 1
    return result