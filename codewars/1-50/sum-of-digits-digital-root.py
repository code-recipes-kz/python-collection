# Функция digital_root принимает на вход целое неотрицательное число n и вычисляет его цифровой корень, используя алгоритм, описанный в задаче. Сначала мы проверяем, имеет ли число более одной цифры, и если да, то суммируем все его цифры. Это продолжается до тех пор, пока число не будет иметь только одну цифру, тогда мы возвращаем полученный результат. В примерах использования мы выводим результаты вычислений для нескольких чисел, используя функцию print.

# tags: Mathematics, Algorithms
# kyu: 6

def digital_root(n):
    """
    Функция принимает на вход целое неотрицательное число n
    и вычисляет его цифровой корень.
    """
    # Пока число n имеет более одной цифры, суммируем его цифры.
    while n >= 10:
        n = sum(int(digit) for digit in str(n))
    # Возвращаем полученный результат.
    return n
