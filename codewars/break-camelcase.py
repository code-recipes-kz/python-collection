"""
Функция break_camel_case() принимает входную строку s и проходит по ее символам. Если символ является заглавной буквой, то перед ним добавляется пробел. В итоге формируется результирующая строка, в которой слова разделены пробелами в соответствии с условиями задачи.

Создание функции break_camel_case(s), которая принимает входную строку s в качестве аргумента.
Инициализация пустой строки result, которая будет использоваться для формирования результата.
Итерация по символам во входной строке s с использованием цикла for char in s.
Проверка каждого символа на то, является ли он заглавной буквой, с помощью метода isupper(). Если символ является заглавной буквой, то выполняется следующий шаг.
Добавление пробела и заглавной буквы в результирующую строку result с помощью оператора конкатенации строк result += " " + char. Это создает разделение между словами в camelCase.
Если символ не является заглавной буквой, то он просто добавляется в результирующую строку result с помощью оператора конкатенации строк result += char.
Повторение шагов 4-6 для всех символов во входной строке s.
Возврат результирующей строки result после завершения цикла.
Примеры использования функции break_camel_case() с различными входными строками и выводом результата на экран с использованием функции print().
"""

# tags: Strings, Fundamentals
# kyu: 6

def solution(s):
    """
    Функция принимает строку s и разделяет слова в camelCase с использованием пробелов.
    """
    result = ""
    for char in s:
        if char.isupper():
            result += " " + char
        else:
            result += char
    return result