# Обратите внимание, что я использую метод lower() для приведения всех символов в строке к нижнему регистру, чтобы не различать заглавные и прописные буквы при подсчете повторяющихся символов. Затем я прохожу по каждому символу в строке и использую метод count() для подсчета количества его вхождений в строку. Если символ встречается только один раз, я добавляю в результирующую строку "(", в противном случае добавляю ")". Наконец, я возвращаю результирующую строку.

def duplicate_encode(word):
    word = word.lower()
    result = ""
    for char in word:
        if word.count(char) == 1:
            result += "("
        else:
            result += ")"
    return result
