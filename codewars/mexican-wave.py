"""
Функция wave принимает на вход строку word и возвращает список строк, представляющих собой волну из заглавных букв, где каждая строка соответствует одному человеку, стоящему в волне. В цикле проходим по каждому символу в строке word, пропуская пробелы, и заменяем букву на заглавную в соответствующей позиции. Затем добавляем полученное слово в список wave_list и возвращаем его.

Функция wave принимает на вход строку word.

Создается пустой список wave_list, который будет содержать результат - список строк с волнами.

В цикле for проходим по каждому индексу i в диапазоне от 0 до длины строки word с помощью функции range(len(word)). Индекс i соответствует текущей позиции в строке word.

Внутри цикла проверяем, является ли символ в позиции i пробелом с помощью метода isspace().

Если символ является пробелом, то пропускаем его с помощью оператора continue, переходим к следующей итерации цикла.

Если символ не является пробелом, то создаем новую строку wave_word путем объединения трех частей:

Срез строки word от начала до i, который соответствует символам перед текущей позицией.
Заглавной буквы, полученной путем вызова метода upper() на символе в позиции i.
Среза строки word от i+1 до конца, который соответствует символам после текущей позиции.
Таким образом, заменяем текущий символ на заглавную букву.

Полученную wave_word добавляем в список wave_list с помощью метода append().

После завершения цикла возвращаем список wave_list, содержащий все строки с волнами, где каждая строка соответствует одному человеку, стоящему в волне.

Пример использования функции wave с аргументом "hello":

Сначала создается пустой список wave_list.
Затем первый символ "h" заменяется на заглавную букву "H", и полученное слово "Hello" добавляется в wave_list.
Далее второй символ "e" заменяется на заглавную букву "E", и полученное слово "hEllo" добавляется в wave_list.
Таким образом, аналогично заменяются остальные символы в строке "hello", и в итоге wave_list содержит ['Hello', 'hEllo', 'heLlo', 'helLo', 'hellO'].
"""

# tags: Arrays, Strings, Fundamentals
# kyu: 6

def wave(word):
    """
    Функция, которая принимает на вход строку word и возвращает список строк, представляющих собой волну
    из заглавных букв, где каждая строка соответствует одному человеку, стоящему в волне.
    """
    wave_list = []
    for i in range(len(word)):
        if word[i].isspace():  # Пропускаем пробелы
            continue
        wave_word = word[:i] + word[i].upper() + word[i+1:]  # Заменяем букву на заглавную
        wave_list.append(wave_word)
    return wave_list