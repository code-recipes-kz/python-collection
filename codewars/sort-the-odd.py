"""
Программа создает новый массив, в котором нечетные числа отсортированы в возрастающем порядке, используя функцию sort() встроенную в Python. Затем она заменяет нечетные числа в исходном массиве на отсортированные нечетные числа с помощью спискового выражения и функции pop() для удаления использованных элементов из списка отсортированных нечетных чисел.
"""

# tags: Fundamentals, Arrays, Sorting
# kyu: 6

def sort_array(arr):
    # Создаем список, содержащий только нечетные числа из исходного массива
    odd_numbers = [x for x in arr if x % 2 != 0]
    # Сортируем нечетные числа в возрастающем порядке
    odd_numbers.sort()
    # Создаем новый массив, в котором нечетные числа заменены отсортированными нечетными числами
    sorted_arr = [x if x % 2 == 0 else odd_numbers.pop(0) for x in arr]
    return sorted_arr