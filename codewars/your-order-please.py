"""
Создание функции sort_string, которая принимает на вход один аргумент - входную строку input_str.
Проверка, является ли входная строка пустой строкой с помощью условного оператора if и оператора not, который возвращает True, если строка пустая, и False в противном случае.
Если входная строка пустая, то функция возвращает пустую строку "" с помощью оператора return и завершает свою работу.
Если входная строка не пустая, то она разделяется на слова с помощью метода split(), который разделяет строку по пробелам и создает список words, содержащий отдельные слова из входной строки.
Создается пустой список sorted_words длиной, равной количеству слов в words, с помощью оператора [""] * len(words). Этот список будет содержать отсортированные слова в итоговом порядке.
Далее, программа проходит циклом for по каждому слову word из списка words.
Внутри цикла for происходит еще один цикл for по каждому символу char в текущем слове word.
С помощью метода isdigit() проверяется, является ли текущий символ char числом.
Если текущий символ char является числом, то оно извлекается с помощью int(char) и вычитается 1 (так как числа в задаче начинаются с 1, а не с 0) для использования в качестве индекса в списке sorted_words.
Текущее слово word добавляется в список sorted_words на соответствующую позицию, определенную числом из слова, с использованием индекса, полученного на предыдущем шаге.
После завершения внутреннего цикла for для символов, внешний цикл for для слов words заканчивается.
Итоговый список sorted_words содержит отсортированные слова в соответствии с числами из входной строки.
Используя метод join(), слова из списка sorted_words соединяются в строку с помощью пробелов между ними.
Полученная отсортированная строка sorted_str возвращается с помощью оператора return в качестве результата работы функции sort_string.
"""

# tags: Strings, Fundamentals
# kyu: 6

def sort_string(input_str):
    if not input_str:  # Проверка на пустую строку
        return ""

    words = input_str.split()  # Разделение строки на слова
    sorted_words = [""] * len(words)  # Создание пустого списка такой же длины, как количество слов

    for word in words:
        for char in word:
            if char.isdigit():
                # Получение числа из слова и использование его в качестве индекса в списке sorted_words
                sorted_words[int(char) - 1] = word

    return " ".join(sorted_words)  # Соединение слов в строку с пробелами между ними
